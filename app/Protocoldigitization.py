# render_static_docx.py
from docxtpl import DocxTemplate

def render_docx(message: str):
    doc = DocxTemplate("template.docx")

    context = {
        "inclusion_criterion": message
    }

    doc.render(context)
    output_path = "filled_document.docx"
    doc.save(output_path)

    print(f"✅ Rendered successfully → {output_path}")

if __name__ == "__main__":
    render_docx("This is a sample inclusion criterion generated by LLM.")